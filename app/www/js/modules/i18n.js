Lumina.I18n.t = (key, ...args) => {
    const text = Lumina.I18n.data[Lumina.State.settings.language]?.[key] || Lumina.I18n.data.zh[key] || key;
    return args.reduce((str, arg, i) => str.replace(`$${i + 1}`, arg), text);
};

